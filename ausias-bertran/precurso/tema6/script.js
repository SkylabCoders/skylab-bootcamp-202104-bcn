function getAnswer( iteratorValue ) { //  Chequear respuesta a la pregunta del turno actual
    
    let userAnswer = answerBox.value ;
    
    userAnswer = trimAndLowerCaseAnswer( userAnswer ) ;
    
    if ( userAnswer === rosco.questions[ iteratorValue ].answer ) {   //  Si la respuesta es correcta
        
        // answerBox.style.visibility = "hidden" ;
        // button.innerHTML = "➡  ACEPTAR  ⬅" ;
        
        rosco.questions[ iteratorValue ].status = 2 ;
        
        letterCircles[ iteratorValue ].style.backgroundColor = `${ getCircleColor ( iteratorValue ) }` ;
        
        
        upDateQuestionsAvailablesIteratorValue() ;
        
        questionsAvailablesIteratorValue.pop() ;
        
        resultBox.innerHTML = '👍' ;    //    <<<<<<<<<<<<<<<<<<<<<<<<<<<<
        resultBox.style.display = "flex" ;
        setTimeout( showResult , 1000 );
        // isQuestion = true ;
        
        let endGameBreak = endGame();
        if ( endGameBreak === null ) { return null ; }
        
    } else if ( userAnswer === "pasapalabra" || userAnswer === "" || userAnswer === null ) {   //  Varias maneras de pasar turno
        
        upDateQuestionsAvailablesIteratorValue() ;
        
        getQuestion() ;
        
    } else if ( userAnswer === "end" || userAnswer === "fin" || userAnswer === "del" ) {     //   Parar juego
                
        showScore() ;
        
    } else {  // respuesta incorrecta
                
        rosco.questions[ iteratorValue ].status = 3 ;
        
        letterCircles[ iteratorValue ].style.backgroundColor = `${ getCircleColor ( iteratorValue ) }`;
        
        let trueAnswer = rosco.questions[ iteratorValue ].answer.toUpperCase() ;
        
        resultBox.innerHTML = `💩`;    ///    <<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<
        resultBox.style.display = "flex" ;

        button.innerHTML = `${ trueAnswer }` ;
        button.style.color = "darkorange" ;
        button.style.backgroundColor = "red" ;




        setTimeout( showResult , 800 );
        upDateQuestionsAvailablesIteratorValue() ;
        
        questionsAvailablesIteratorValue.pop() ;
         
        // isQuestion = true ;
        
        let endGameBreak = endGame();
        if ( endGameBreak === null ) { return null ; }
    }
    
    answerBox.value = "" ;  // Resetear texto de la caja de la respuesta
    answerBox.focus() ;
}


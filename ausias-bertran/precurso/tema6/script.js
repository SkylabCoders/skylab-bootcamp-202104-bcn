function getSetQuestion(libraryIterator) {  //  seleccionar una pregunta de cada letra
    
    let questionsAvailable = [ ];  //  Biblioteca de valores de posicionamiento ( la "i" de  questionsLibrary[i] ) de las preguntas disponibles
    
    let stillAvailabes = 0;
    
    
    for ( let i = 0 ; i < rosco.questionsLibrary [ libraryIterator ] .length ; i++ ) {   //   Sumar total de preguntas disponibles (que no se hayan mostrado)
        
        if ( rosco.questionsLibrary [ libraryIterator ][ i ] .status === 0 ) {
            
            stillAvailabes++ ;            
            }
        }

        
        if ( stillAvailabes === 0 ) {   //  Asegurar que existan preguntas disponibles. En caso contrario habilitar todas las preguntas como disponibles

            for ( let i = 0 ; i < rosco.questionsLibrary [ libraryIterator ] .length ; i++ ) {

                rosco.questionsLibrary [ libraryIterator ][ i ] .status = 0;

            }
        }
        
        for ( let i = 0 ; i < rosco.questionsLibrary [ libraryIterator ] .length ; i++ ) {   //  Agrupar preguntas disponibles
 
            if ( rosco.questionsLibrary [ libraryIterator ][ i ] .status === 0 ) {
   
                questionsAvailable.push ( i );   //  Guardar posicion de las preguntas disponibles

            }
        }

    let selectedQuestion = questionsAvailable.length;                           //  Seleccionar cualquiera de las preguntas disponibles a través de su posicion en el array
    selectedQuestion = Math.floor ( Math.random () * selectedQuestion );
    selectedQuestion = questionsAvailable [ selectedQuestion ];

    rosco.questions.push ( rosco.questionsLibrary [ libraryIterator ][ selectedQuestion ] );  
    rosco.questionsLibrary [ libraryIterator ][ selectedQuestion ] .status = 1;     //  Para recordar las preguntas que ya han sido seleccionadas

}


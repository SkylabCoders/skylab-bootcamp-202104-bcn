function startTimer () {
    
    if ( rosco.isTimeActive === false ) {
        
        rosco.isTimeActive = true ;
        minutesCounter = 15 ;
        secondsCounter = 0 ;
        
        startTimerStartFunction = setInterval(
            
            function () {
                
                if ( rosco.timeIsRunning === true) {             //  Pausar cronometro      

                    if ( rosco.catchTime === true && rosco.userTime === "" ) {   //   Capturar temps per Ranking

                        rosco.userTime = (minutesCounter.toString()).padStart( 2 , "0" ) + ":" + (secondsCounter.toString()).padStart( 2 , "0" ) ;

                        timer.style.display = "none" ;
                        menu.style.display = "none" ;

                        return null ;
                    }
                    if (secondsCounter === -1) {    

                        secondsCounter = 59 ;                    
                        minutesCounter-- ;
                        let minutesCounterString = minutesCounter.toString() ;
                        minutes.innerHTML = minutesCounterString.padStart( 2 , "0") + ":" ;

                        if ( minutesCounter <= 0 ) {

                            let rankingMinutes = 15 - minutesCounter ;
                            let rankingSeconds = 59 - secondsCounter ;


                            secondsCounter = 0 ;
                            minutesCounter = 0 ;
                            minutes.innerHTML = minutesCounterString.padStart( 2 , "0") + ":" ;


                            rosco.userTime = `${rankingMinutes.padStart( 2 , "0") }:${ rankingSeconds.padStart( 2 , "0") }` ;  
                            rosco.isTimeActive = false ;
                            rosco.catchTime = true ;

                            delayShowScore(  );

                            timer.style.display = "none" ;
                            menu.style.display = "none" ;

                            return null ;

                        }                    
                    }


                    let secondsCounterString = secondsCounter.toString() ;
                    seconds.innerHTML = secondsCounterString.padStart( 2 , "0") ;

                    secondsCounter-- ;        
                    
                } else {}
            }
            , 1000 
        ) ;
    }
}






















const initializeBoard = (width, height) => {
  const initialArray = [];
  if (width == null || height == null) {
    width = 10;
    height = 10;
  }
  for (let i = 0; i < width; i++) {
    const row = [];
    for (let j = 0; j < height; j++) {
      row.push(0);
    }
    initialArray.push(row);
  }
  return initialArray;
};

